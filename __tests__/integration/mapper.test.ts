function calculateLineCoverage(coverage) {
  const {
    f, fnMap, b, branchMap,
  } = coverage;
  const functionCoverage = convertFunctionCoverage(fnMap, f);

  const branchCoverage = convertBranchCoverage(branchMap, b);

  branchCoverage.forEach(b => {
    const functions = functionCoverage.filter(
      f => b.start.line >= f.start.line && b.end.line <= f.end.line,
    );

    console.log(functions);
  });

  console.log(f);
}

function convertFunctionCoverage(fnMap: object, f: any) {
  return Object.entries(fnMap).map(([k, { name, decl }]) => {
    const hits = f[k];
    return {
      name,
      hits,
      start: decl.start,
      end: decl.end,
    };
  });
}

function convertBranchCoverage(branchMap: object, b: any) {
  return Object.entries(branchMap).map(([k, { type, loc }]) => {
    const hits = b[k];
    return {
      type,
      hits,
      start: loc.start,
      end: loc.end,
    };
  });
}
